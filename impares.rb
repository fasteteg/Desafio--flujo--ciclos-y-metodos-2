i = 1
while i <= 20
  if i % 2 != 0
    puts i
  end
  i += 1
end

# Este código utilizará un bucle while para recorrer los números comprendidos entre 0 y 20. Para cada número, 
# se verifica si es impar utilizando el operador %, que devuelve el resto de dividir el número entre 2. 
# Si el resto es diferente de 0, entonces el número es impar y se imprime en la consola. Después de cada iteración, se aumenta el contador i en 1 para pasar al siguiente número.